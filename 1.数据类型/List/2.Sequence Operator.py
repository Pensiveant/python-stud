'''
序列操作：连接、重复、索引、切片
'''

# --------连接----------

list1 = [1, 2, 3]+[4, 5, 6]  # 连接,[1, 2, 3, 4, 5, 6]
# 连接需要两边的类型相同，否则报错
# list2 = [1, 2]+'34'  # 报错
string1 = str([1, 2])+'34'  # '[1,2]34',等价于'[1,2]'+'34'
list3 = [1, 2]+list('34')  # [1, 2, '3', '4'],等价于[1,2]+["3","4"]


# -----重复---------
list4 = ['Hi!']*4  # 重复，['Hi!','Hi!','Hi!','Hi!']

# ----------索引------------
L = ['spam', 'Spam', 'SPAM!']
L[2]  # 'SPAM!'，偏移量从0开始
L[-2]  # 'Spam'，负数，从右边开始

# -----------切片-----------
L1 = [0,1,2,3,4,5,6]
L1[1:]  # [1,2,3,4,5,6]
L1[:1]  # [0]
L1[:4:-1] # [6,5]
print('---------')
